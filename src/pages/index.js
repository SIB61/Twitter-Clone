import Head from "next/head";
import styles from "@/styles/Home.module.css";
import { getServerSession } from "next-auth";
import { createOptions } from "./api/auth/[...nextauth]";
import { getAllTweets } from "@/lib/services/tweet/get-tweet.server";
import { MainLayout } from "@/components/layouts/main-layout/main-layout";
import { TweetList } from "@/components/tweet/tweet-list/TweetList";
import { AuthCard } from "@/components/auth/auth-card/AuthCard";
import { dbConnect } from "@/lib/helpers/db";

export async function getServerSideProps(context) {
  await dbConnect();
  const session = await getServerSession(context.req, context.res, createOptions(context.req));
  const allTweets = await getAllTweets();
  if (session) {
    return {
      redirect: {
        destination: "/home",
        permanent: false,
      },
    };
  }
  return {
    props: JSON.parse(
      JSON.stringify({
        tweets: allTweets,
      })
    ),
  };
}

function Page({ tweets }) {
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <MainLayout>
        <div className={styles.home}>
          <div className="center-container">
            <div className="appbar">Explore</div>
            <TweetList tweets={tweets} />
          </div>
          <div className={styles.rightBar}>
            <AuthCard />
          </div>
        </div>
      </MainLayout>
    </>
  );
}

export default Page;
